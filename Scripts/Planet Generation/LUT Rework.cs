                    {
                        lastMajorRadius = ringradius;
                        lastMajorRadiusCount = (int)(ringradius / 4.0) * 4;
                    }

                    lut[cnt] = lastMajorRadiusCount;
                    classicLUT[classicIdx] = lastMajorRadiusCount;
